public class Major {
    private String name;
    private int ref;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getRef() {
        return ref;
    }
    public void setRef(int ref) {
        this.ref = ref;
    }
    public Major(String name, int ref) {
        this.name = name;
        this.ref = ref;
    }
    
}
